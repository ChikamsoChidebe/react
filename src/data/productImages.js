// Get a random fallback image
export const getRandomFallbackImage = () => {
  const fallbacks = productImages.fallback;
  return fallbacks[Math.floor(Math.random() * fallbacks.length)];
};

// Get random product images by category
export const getRandomProductImages = (category = 'electronics', count = 3) => {
  const categoryImages = productImages[category] || productImages.electronics;
  const shuffled = [...categoryImages].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count).map(img => img.url);
};